package Estrutura_Repeticao;

public class While {
	
	public static void main(String[] args) {
		
		int contador = 1;
		
		//while - enquanto
		while(contador <= 10) {
			
			System.out.println("Numero: " + contador);
			
			contador+=2;
			
			//contador++ acrescentanto 1 no contador
			
		}
		
	}

}
