package Estrutura_Repeticao;

public class DoWhile {
	
	public static void main(String[] args) {
		
		int contador = 1;
		
		//do - primeiro / faça / execute
		do {
			
			System.out.println("Número: " + contador);
			
			contador++;
		
		//enquanto
		}while(contador <= 10);
		
	}

}
